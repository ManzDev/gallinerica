export const shuffle = (array) => {
  let m = array.length;
  let t;
  let i;

  while (m) {
    // Pick a remaining elementâ€¦
    i = Math.floor(Math.random() * m--);

    // And swap it with the current element.
    t = array[m];
    array[m] = array[i];
    array[i] = t;
  }

  return array;
};
